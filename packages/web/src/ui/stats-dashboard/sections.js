// === A. Hero KPIs ======================================================

export function renderKPIs(ctx) {
  const { stats, previous, isAllTime, isGlobal, selected } = ctx;
  const filtered = selected && selected.size > 0;
  const totals = filtered
    ? sumPerModelTotals(stats.per_model, selected)
    : (stats.totals || {});
  const prev = filtered
    ? sumPerModelTotals(previous?.per_model, selected)
    : (previous?.totals || {});
  // Aggregate timeseries doesn't carry per-persona breakdown for mood, so
  // when a filter is engaged we rebuild the hits sparkline from
  // per_model_timeseries (which is per-persona) and drop the mood sparklines
  // to empty rather than fake them. Honest > pretty.
  const buckets = stats.timeseries || [];
  const hasSpark = !filtered && buckets.length >= 2;
  const hSeries = filtered
    ? sumPerModelSeries(stats.per_model_timeseries, selected, 'hits')
    : buckets.map((b) => Number(b.hits || 0));
  const hasFilteredSpark = filtered && hSeries.length >= 2 && hSeries.some((v) => v > 0);
  const helpSeries = hasSpark ? buckets.map((b) => Number(b.help_mood || 0)) : null;
  const hurtSeries = hasSpark ? buckets.map((b) => Number(b.hurt_mood || 0)) : null;
  const playSeries = hasSpark ? buckets.map((b) => Number(b.play_ms || 0)) : null;

  // When filtered, the Hits sparkline uses per-persona series and gets the
  // first-selected persona's accent so the tile reads as belonging to the
  // filter; mood sparklines drop because per_model_timeseries doesn't carry
  // mood. Help Ratio replaces Play Time, since play_ms isn't tracked
  // per-persona.
  const filterAccent = filtered ? personaAccent([...selected][0]) : null;
  const hitsAccent = filtered ? filterAccent : 'var(--ink)';

  const tiles = [];
  if (isGlobal) {
    tiles.push(kpi({
      label: 'Total Hits',
      value: formatN(totals.hits),
      delta: '',
      spark: hasSpark ? hSeries : (hasFilteredSpark ? hSeries : null),
      accent: hitsAccent,
    }));
    tiles.push(kpi({
      label: 'Help',
      value: formatN(totals.help_mood),
      delta: '',
      spark: hasSpark ? helpSeries : null, accent: 'var(--success)',
    }));
    tiles.push(kpi({
      label: 'Hurt',
      value: formatN(totals.hurt_mood),
      delta: '',
      spark: hasSpark ? hurtSeries : null, accent: 'var(--danger)',
    }));
    const totalActivity = (totals.help_mood || 0) + (totals.hurt_mood || 0);
    const helpRatio = totalActivity ? (totals.help_mood || 0) / totalActivity : 0.5;
    tiles.push(kpi({
      label: 'Help Ratio',
      value: `${Math.round(helpRatio * 100)}%`,
      delta: '',
      spark: null, accent: helpRatio >= 0.5 ? 'var(--success)' : 'var(--danger)',
    }));
  } else if (filtered) {
    const totalActivity = (totals.help_mood || 0) + (totals.hurt_mood || 0);
    const helpRatio = totalActivity ? (totals.help_mood || 0) / totalActivity : 0.5;
    tiles.push(kpi({
      label: 'Total Hits',
      value: formatN(totals.hits),
      delta: previous && !isAllTime ? deltaInline(totals.hits, prev.hits) : '',
      spark: hasFilteredSpark ? hSeries : null, accent: hitsAccent,
    }));
    tiles.push(kpi({
      label: 'Help Mood',
      value: formatN(totals.help_mood),
      delta: previous && !isAllTime ? deltaInline(totals.help_mood, prev.help_mood) : '',
      spark: null, accent: 'var(--success)',
    }));
    tiles.push(kpi({
      label: 'Hurt Mood',
      value: formatN(totals.hurt_mood),
      delta: previous && !isAllTime ? deltaInline(totals.hurt_mood, prev.hurt_mood) : '',
      spark: null, accent: 'var(--danger)',
    }));
    tiles.push(kpi({
      label: 'Help Ratio',
      value: totalActivity ? `${Math.round(helpRatio * 100)}%` : '—',
      delta: '',
      spark: null, accent: helpRatio >= 0.5 ? 'var(--success)' : 'var(--danger)',
    }));
  } else {
    tiles.push(kpi({
      label: 'Total Hits',
      value: formatN(totals.hits),
      delta: previous && !isAllTime ? deltaInline(totals.hits, prev.hits) : '',
      spark: hasSpark ? hSeries : null, accent: 'var(--ink)',
    }));
    tiles.push(kpi({
      label: 'Help Mood',
      value: formatN(totals.help_mood),
      delta: previous && !isAllTime ? deltaInline(totals.help_mood, prev.help_mood) : '',
      spark: hasSpark ? helpSeries : null, accent: 'var(--success)',
    }));
    tiles.push(kpi({
      label: 'Hurt Mood',
      value: formatN(totals.hurt_mood),
      delta: previous && !isAllTime ? deltaInline(totals.hurt_mood, prev.hurt_mood) : '',
      spark: hasSpark ? hurtSeries : null, accent: 'var(--danger)',
    }));
    tiles.push(kpi({
      label: 'Play Time',
      value: formatDuration(totals.play_ms),
      delta: previous && !isAllTime ? deltaInline(totals.play_ms, prev.play_ms) : '',
      spark: hasSpark ? playSeries : null, accent: 'var(--info)',
    }));
  }
  const eyebrow = isGlobal ? 'Across every player' : (filtered ? 'Filtered to your selected personas' : 'Your headline numbers');
  return `
    <section class="sd-section" data-section="kpi" data-filtered="${filtered ? '1' : '0'}">
      <header class="sd-section-head">
        <div>
          <div class="sd-section-eyebrow">${escapeHTML(eyebrow)}</div>
          <h2 class="sd-section-title">Overview</h2>
        </div>
      </header>
      <div class="sd-section-body">
        <div class="sd-kpi-row">${tiles.join('')}</div>
        ${renderBenchmarkStrip(ctx, totals)}
        ${filterScopeNote(ctx, { hint: filtered ? 'Headline numbers follow your buddy filter. Mood sparklines stay hidden because mood is only stored for the full set.' : null })}
      </div>
    </section>
  `;
}

// Single-line "vs global" comparison rendered below the overview KPIs when the
// benchmark toggle is on. Compares help-ratio directly (cleanly comparable
// across vastly different volumes) and notes the most-targeted persona
// globally, the two highest-signal facts derivable from leaderboard totals.
function renderBenchmarkStrip(ctx, totals) {
  const { benchmark, isGlobal, selected } = ctx;
  if (!benchmark || isGlobal) return '';
  // When a persona filter is engaged, restrict the global side to the same
  // subset so the comparison is apples-to-apples. Otherwise the user sees
  // their filtered ratio compared against an unfiltered global ratio, which
  // is misleading.
  const filtered = selected && selected.size > 0;
  const gTotals = filtered
    ? sumPerModelTotals(benchmark.per_model, selected)
    : (benchmark.totals || {});
  const personalActivity = (totals.help_mood || 0) + (totals.hurt_mood || 0);
  const globalActivity = (gTotals.help_mood || 0) + (gTotals.hurt_mood || 0);
  if (globalActivity === 0) return '';
  const personalRatio = personalActivity ? (totals.help_mood || 0) / personalActivity : null;
  const globalRatio = (gTotals.help_mood || 0) / globalActivity;
  const gPerModel = benchmark.per_model || {};
  let topId = null;
  let topHits = 0;
  for (const [id, m] of Object.entries(gPerModel)) {
    const hits = Number(m?.hits || 0);
    if (hits > topHits) { topHits = hits; topId = id; }
  }
  const topLabel = topId ? PERSONA_LABELS[topId] || topId : null;
  const topShare = topId && globalActivity
    ? Math.round((Number(gPerModel[topId]?.hits || 0) / Math.max(1, Number(gTotals.hits || 0))) * 100)
    : null;

  const ratioPct = (r) => `${Math.round(r * 100)}%`;
  const ratioCmp = personalRatio == null ? '' : (() => {
    const delta = Math.round((personalRatio - globalRatio) * 100);
    const sign = delta > 0 ? '+' : '';
    const dir = delta >= 0 ? 'kinder' : 'rougher';
    return `<span class="sd-bench-delta ${delta >= 0 ? 'pos' : 'neg'}">${sign}${delta}pp ${dir} than global</span>`;
  })();
  const topFragment = topLabel && topShare
    ? `<span class="sd-bench-fact">Globally most-targeted: <strong>${escapeHTML(topLabel)}</strong> (${topShare}% of hits)</span>`
    : '';
  return `
    <div class="sd-bench-strip" data-bench-strip>
      <span class="sd-bench-pill">
        <span class="sd-bench-pill-eyebrow">Global help ratio</span>
        <span class="sd-bench-pill-value">${ratioPct(globalRatio)}</span>
      </span>
      ${ratioCmp}
      ${topFragment}
    </div>
  `;
}

function kpi({ label, value, delta, spark, accent }) {
  const sparkHtml = Array.isArray(spark) && spark.length >= 2
    ? gradientSparkline(spark, { accent, height: 56, lineWidth: 1.5 })
    : `<svg class="sd-spark sd-spark-empty" viewBox="0 0 240 56" preserveAspectRatio="none" aria-hidden="true">
         <line x1="0" y1="40" x2="240" y2="40" stroke="${accent}" stroke-opacity=".18" stroke-dasharray="2 4" stroke-width="1" vector-effect="non-scaling-stroke"/>
       </svg>`;
  return `
    <div class="sd-kpi">
      <div class="sd-kpi-eyebrow">${escapeHTML(label)}</div>
      <div class="sd-kpi-row-2">
        <div class="sd-kpi-num">${value}</div>
        ${delta ? `<div class="sd-kpi-delta">${delta}</div>` : ''}
      </div>
      <div class="sd-kpi-spark" style="--accent:${accent};">${sparkHtml}</div>
    </div>
  `;
}

