// === F. Persona ladder ===============================================

export function renderPersonaLadder(ctx) {
  const { selected, benchmark, isGlobal } = ctx;
  const rows = insightFacts(ctx).buddies.rows;

  if (rows.length === 0) {
    const insight = sectionInsight('buddies', ctx, { empty: true });
    return focusedLayout({
      id: 'personas', ctx,
      title: 'Buddies',
      ...insight,
      viz: empty('No buddy data yet.'),
    });
  }

  // Benchmark math: personalShare = how much of YOUR attention goes to this
  // persona; globalShare = how much of GLOBAL attention goes to them. Delta
  // (in percentage points) tells the user whether they favor that persona
  // more or less than the crowd does. Rendered as an explicit cell so the
  // signal can't hide behind the bright personal bar.
  const personalMax = Math.max(1, ...rows.map((r) => r.total));
  const gPerModel = (!isGlobal && benchmark?.per_model) || null;
  const personalTotal = rows.reduce((s, r) => s + r.total, 0) || 1;
  const globalTotal = gPerModel
    ? Object.values(gPerModel).reduce((s, m) => s + Number(m?.hits || 0), 0)
    : 0;
  const showDelta = gPerModel && globalTotal > 0;
  const html = rows.map((r) => {
    const accent = personaAccent(r.id);
    const helpBase = r.help + r.hurt || 1;
    const helpPct = (r.help / helpBase) * 100;
    const hurtPct = (r.hurt / helpBase) * 100;
    const widthPct = (r.total / personalMax) * 100;
    const dim = selected.size > 0 && !selected.has(r.id);
    const fav = r.fav ? (TOOLS_BY_ID[r.fav]?.label || r.fav) : '—';
    let deltaCell = '';
    if (showDelta) {
      const personalShare = r.total / personalTotal;
      const globalShare = Number(gPerModel[r.id]?.hits || 0) / globalTotal;
      const deltaPp = Math.round((personalShare - globalShare) * 100);
      const sign = deltaPp > 0 ? '+' : '';
      const cls = deltaPp > 0 ? 'pos' : deltaPp < 0 ? 'neg' : 'zero';
      deltaCell = `<div class="sd-ladder-delta ${cls}" title="vs global average">${sign}${deltaPp}pp</div>`;
    }
    return `
      <div class="sd-ladder-row ${showDelta ? 'has-delta' : ''}" data-ladder-row="${r.id}" data-dim="${dim ? '1' : '0'}">
        <div class="sd-ladder-name">
          <span class="sd-ladder-dot" style="background:${accent};"></span>
          ${escapeHTML(PERSONA_LABELS[r.id])}
        </div>
        <div class="sd-ladder-bar" style="width:${widthPct.toFixed(1)}%;">
          <i class="sd-ladder-bar-help" style="width:${helpPct.toFixed(1)}%;"></i>
          <i class="sd-ladder-bar-hurt" style="width:${hurtPct.toFixed(1)}%; left:${helpPct.toFixed(1)}%;"></i>
        </div>
        <div class="sd-ladder-num">${formatN(r.total)}</div>
        ${deltaCell}
        <div class="sd-ladder-fav">${escapeHTML(fav)}</div>
      </div>
    `;
  }).join('');

  const insight = sectionInsight('buddies', ctx);
  const baseNote = 'Each row is clickable. __Width|Bar width = that buddy\'s interactions divided by the most-played buddy\'s interactions in this view.__ shows total attention. The inner colors show how much of that play was helpful versus harmful.';
  const benchNote = showDelta
    ? ' The **vs global** column shows how your share of attention compares to the average player: __+Npp|You give this buddy N percentage points more of your attention than the average player does.__ means you favor that buddy more, **−Npp** means less.'
    : '';
  return focusedLayout({
    id: 'personas', ctx,
    title: 'Buddies',
    ...insight,
    vizNote: baseNote + benchNote,
    viz: `<div class="sd-ladder">${html}</div>`,
  });
}

