// Given per_model_timeseries rows ({model, bucket_start, fires, hits}),
// return a single time-aligned series summing `field` across selected
// personas. Buckets present anywhere in the data become the x-axis.
function sumPerModelSeries(perModelBuckets, selected, field) {
  if (!Array.isArray(perModelBuckets) || perModelBuckets.length === 0) return [];
  const xVals = [...new Set(perModelBuckets.map((b) => b.bucket_start))]
    .sort((a, b) => a - b);
  const idx = new Map(xVals.map((x, i) => [x, i]));
  const out = new Array(xVals.length).fill(0);
  for (const row of perModelBuckets) {
    if (!selected.has(row.model)) continue;
    const i = idx.get(row.bucket_start);
    out[i] = (out[i] || 0) + Number(row[field] || 0);
  }
  return out;
}

function filterScopeNote(ctx, { hint } = {}) {
  if (!hint) return '';
  return `<div class="sd-filter-note"><span class="sd-filter-note-bullet"></span>${escapeHTML(hint)}</div>`;
}
