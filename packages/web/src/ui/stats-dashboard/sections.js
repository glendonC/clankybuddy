// Theme-aware neutral accent for the `_all` view. Returns a concrete hex
// so it can be used in SVG `stop-color` (which doesn't reliably resolve
// chained `var()` references through inline styles) and in JS-built
// color-mix strings.
function neutralAccentHex() {
  if (typeof document === 'undefined') return '#18181b';
  return document.body?.classList?.contains('theme-light') ? '#18181b' : '#ededf0';
}

function anatomySingle(heatmap, modelId, mini = false) {
  const filtered = modelId === '_all' ? heatmap : heatmap.filter((r) => r.character === modelId);
  const counts = { head: 0, torso: 0, arm: 0, leg: 0 };
  for (const r of filtered) counts[r.part] = (counts[r.part] || 0) + (r.count || 0);
  const total = counts.head + counts.torso + counts.arm + counts.leg;
  const max = Math.max(1, counts.head, counts.torso, counts.arm, counts.leg);
  const accent = modelId === '_all' ? neutralAccentHex() : personaAccent(modelId);
  const fIntensity = (count) => max ? Math.min(1, count / max) : 0;
  const headI = fIntensity(counts.head);
  const torsoI = fIntensity(counts.torso);
  const armI = fIntensity(counts.arm);
  const legI = fIntensity(counts.leg);

  const W = mini ? 180 : 280;
  const H = mini ? 250 : 380;
  const bloomId = `sd-bloom-${modelId}-${mini ? 'm' : 's'}-${++_gradUid}`;
  // Geometry mirrors physics/ragdoll.js exactly (head r=36, body r=44, arms
  // r=22 offset ±66 at y+82, legs r=26 offset ±26 at y+162) so the heatmap
  // reads as "this is the buddy" rather than a generic humanoid silhouette.
  // Origin (head center) lives at (100, 56) inside the 200×280 viewBox; the
  // figure spans roughly x=12..188 and y=20..238 with margin all around.
  const armBloomR = 22 + armI * 16;
  const legBloomR = 26 + legI * 18;
  const svg = `
    <svg class="sd-anatomy-svg" viewBox="0 0 200 280" preserveAspectRatio="xMidYMid meet" width="${W}" height="${H}">
      <defs>
        <radialGradient id="${bloomId}" cx="50%" cy="50%">
          <stop offset="0%" stop-color="${accent}" stop-opacity="0.85"/>
          <stop offset="100%" stop-color="${accent}" stop-opacity="0"/>
        </radialGradient>
      </defs>
      <circle cx="100" cy="50"  r="${(36 + headI * 24).toFixed(1)}"  fill="url(#${bloomId})" opacity="${headI.toFixed(3)}"/>
      <circle cx="100" cy="138" r="${(44 + torsoI * 30).toFixed(1)}" fill="url(#${bloomId})" opacity="${torsoI.toFixed(3)}"/>
      <circle cx="34"  cy="132" r="${armBloomR.toFixed(1)}"          fill="url(#${bloomId})" opacity="${armI.toFixed(3)}"/>
      <circle cx="166" cy="132" r="${armBloomR.toFixed(1)}"          fill="url(#${bloomId})" opacity="${armI.toFixed(3)}"/>
      <circle cx="74"  cy="212" r="${legBloomR.toFixed(1)}"          fill="url(#${bloomId})" opacity="${legI.toFixed(3)}"/>
      <circle cx="126" cy="212" r="${legBloomR.toFixed(1)}"          fill="url(#${bloomId})" opacity="${legI.toFixed(3)}"/>
      <g class="sd-anatomy-silhouette">
        <circle cx="100" cy="50"  r="36"/>
        <circle cx="100" cy="138" r="44"/>
        <circle cx="34"  cy="132" r="22"/>
        <circle cx="166" cy="132" r="22"/>
        <circle cx="74"  cy="212" r="26"/>
        <circle cx="126" cy="212" r="26"/>
      </g>
      <g class="sd-anatomy-hits">
        <circle cx="100" cy="50"  r="36" data-tip="${escapeHTML(`Head\n${formatN(counts.head)} hits`)}"/>
        <circle cx="100" cy="138" r="44" data-tip="${escapeHTML(`Torso\n${formatN(counts.torso)} hits`)}"/>
        <circle cx="34"  cy="132" r="22" data-tip="${escapeHTML(`Arms\n${formatN(counts.arm)} hits`)}"/>
        <circle cx="166" cy="132" r="22" data-tip="${escapeHTML(`Arms\n${formatN(counts.arm)} hits`)}"/>
        <circle cx="74"  cy="212" r="26" data-tip="${escapeHTML(`Legs\n${formatN(counts.leg)} hits`)}"/>
        <circle cx="126" cy="212" r="26" data-tip="${escapeHTML(`Legs\n${formatN(counts.leg)} hits`)}"/>
      </g>
    </svg>
  `;

  // Dominance + inline stacked bar replaces the old vertical list. The
  // ranked-by-count order encodes the hierarchy, and the bar's widths
  // encode share, so a glance tells you both "which part takes it" and
  // "how lopsided the distribution is." Inline labels render inside wide
  // segments and disappear (with a hover tooltip) on narrow ones.
  const ranked = [
    ['Head', counts.head], ['Torso', counts.torso],
    ['Arms', counts.arm], ['Legs', counts.leg],
  ].sort((a, b) => b[1] - a[1]);
  const [topLabel, topCount] = ranked[0];
  const topPct = total ? Math.round((topCount / total) * 100) : 0;
  const segments = ranked.map(([label, n], i) => {
    const pct = total ? (n / total) * 100 : 0;
    return { label, n, pct, rank: i };
  });
  // Bake colors at render time so SVG stop-color + CSS color-mix both
  // receive a literal value (chained CSS vars through inline styles are
  // unreliable across browsers in these contexts).
  const segTone = [1, 0.62, 0.38, 0.22];
  const segBg = (rank) => `color-mix(in srgb, ${accent} ${Math.round(segTone[rank] * 100)}%, var(--sd-surface))`;
  const stackHTML = segments.map((s) => {
    const wideEnough = s.pct >= 11;
    const textColor = s.rank <= 1 ? '#fff' : 'var(--sd-ink)';
    return `
      <span class="sd-anatomy-seg" data-rank="${s.rank}"
            style="flex-grow:${s.pct.toFixed(2)}; background:${segBg(s.rank)}; color:${textColor};"
            data-tip="${escapeHTML(`${s.label}\n${formatN(s.n)} hits · ${Math.round(s.pct)}%`)}">
        ${wideEnough
          ? `<span class="sd-anatomy-seg-inner"><span class="sd-anatomy-seg-label">${escapeHTML(s.label)}</span><span class="sd-anatomy-seg-pct">${Math.round(s.pct)}%</span></span>`
          : ''}
      </span>
    `;
  }).join('');
  const runners = segments.slice(1).map((s) => `
    <div class="sd-anatomy-runner">
      <span class="sd-anatomy-runner-dot" style="background:${segBg(s.rank)};"></span>
      <span class="sd-anatomy-runner-label">${escapeHTML(s.label)}</span>
      <span class="sd-anatomy-runner-num">${formatN(s.n)}</span>
    </div>
  `).join('');

  return `
    <div class="sd-anatomy ${mini ? 'sd-anatomy-mini' : ''}" style="--accent:${accent};">
      <div class="sd-anatomy-figure">${svg}</div>
      <div class="sd-anatomy-stats">
        ${mini ? `<div class="sd-anatomy-mini-name">${escapeHTML(modelId === '_all' ? 'all' : PERSONA_LABELS[modelId])}</div>` : ''}
        <div class="sd-anatomy-headline">
          <div class="sd-anatomy-eyebrow">Heaviest part</div>
          <div class="sd-anatomy-hero" style="color:${accent};">${escapeHTML(topLabel)}</div>
          <div class="sd-anatomy-cap">${formatN(topCount)} hits · ${topPct}% of ${formatN(total)} total</div>
        </div>
        <div class="sd-anatomy-stack" role="presentation">${stackHTML}</div>
        <div class="sd-anatomy-runners">${runners}</div>
      </div>
    </div>
  `;
}

