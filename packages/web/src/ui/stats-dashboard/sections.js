export function renderPulse(ctx) {
  const { stats, selected } = ctx;
  const buckets = stats.timeseries || [];
  if (buckets.length < 2) {
    const insight = sectionInsight('activity', ctx, { empty: true });
    return focusedLayout({
      id: 'pulse', ctx,
      title: 'Day by day',
      ...insight,
      viz: empty('Not enough days yet.'),
    });
  }

  const perModelBuckets = stats.per_model_timeseries || [];
  const overlay = buildBenchmarkOverlay(buckets, ctx);
  let body;
  if (selected.size === 0) {
    body = pulseHelpHurt(buckets, overlay);
  } else if (selected.size === 1) {
    body = pulsePerPersona(perModelBuckets, [...selected]);
  } else if (selected.size <= 3) {
    body = pulsePerPersona(perModelBuckets, [...selected]);
  } else {
    body = pulseSmallMultiples(perModelBuckets, [...selected]);
  }

  const insight = sectionInsight('activity', ctx);
  let vizNote = selected.size === 0
    ? '**Green** rises with helpful mood, **red** falls with hurt mood, and the dark line shows __net direction|Net direction = helpful mood minus hurt mood, plotted across the visible range.__.'
    : 'Each line is one selected buddy. Hover the chart for the date and interaction count.';
  if (overlay && selected.size === 0) {
    vizNote += ' The faint dashed lines show the **global rhythm** for the same window, scaled to your axis, compare the shape, not the height.';
  }
  return focusedLayout({
    id: 'pulse', ctx, title: 'Day by day',
    ...insight,
    vizNote,
    viz: body,
  });
}

// Pulls per-day help/hurt counts from ctx.benchmarkSeries, sums them per
// day (optionally restricted to selected personas), then aligns to the
// personal buckets by UTC date. Returns null when the overlay isn't
// applicable (toggle off, series empty, or every personal bucket has zero
// global activity). Hour-grained personal buckets collapse to the same
// day key, that's intentional: a step-flat overlay reads as "this is the
// rhythm of the day overall," not minute-by-minute.
function buildBenchmarkOverlay(personalBuckets, ctx) {
  const { benchmarkSeries, selected } = ctx;
  if (!benchmarkSeries || !Array.isArray(benchmarkSeries.timeseries)) return null;
  if (benchmarkSeries.timeseries.length === 0) return null;
  const filter = selected && selected.size > 0 ? selected : null;
  const dayMap = new Map();
  for (const day of benchmarkSeries.timeseries) {
    if (!day || !day.date || !day.per_model) continue;
    let help = 0;
    let hurt = 0;
    for (const [modelId, m] of Object.entries(day.per_model)) {
      if (filter && !filter.has(modelId)) continue;
      help += Number(m?.help || 0);
      hurt += Number(m?.hurt || 0);
    }
    dayMap.set(day.date, { help, hurt });
  }
  if (dayMap.size === 0) return null;
  const utcDate = (ts) => {
    const d = new Date(Number(ts) || 0);
    if (Number.isNaN(d.getTime())) return null;
    return `${d.getUTCFullYear()}-${String(d.getUTCMonth() + 1).padStart(2, '0')}-${String(d.getUTCDate()).padStart(2, '0')}`;
  };
  const helpSeries = [];
  const hurtSeries = [];
  let nonZero = false;
  for (const b of personalBuckets) {
    const key = utcDate(b.bucket_start);
    const row = key ? dayMap.get(key) : null;
    const help = row ? row.help : 0;
    const hurt = row ? row.hurt : 0;
    if (help > 0 || hurt > 0) nonZero = true;
    helpSeries.push(help);
    hurtSeries.push(hurt);
  }
  if (!nonZero) return null;
  return { helpSeries, hurtSeries };
}

function pulseHelpHurt(buckets, overlay) {
  const help = buckets.map((b) => Math.max(0, Number(b.help_mood || 0)));
  const hurt = buckets.map((b) => Math.max(0, Number(b.hurt_mood || 0)));
  const W = 696;
  const H = 220;            // data area (baseline + curve envelope)
  const tickBand = 22;      // reserved row below the data for x-axis labels
  const totalH = H + tickBand;
  const padX = 4;
  const baseline = H / 2;
  const halfH = H / 2 - 8;
  const maxAbs = Math.max(1, ...help, ...hurt);

  const xAt = (i) => padX + (i / (buckets.length - 1)) * (W - padX * 2);
  const yHelp = (v) => baseline - (v / maxAbs) * halfH;
  const yHurt = (v) => baseline + (v / maxAbs) * halfH;
  const helpPts = help.map((v, i) => ({ x: xAt(i), y: yHelp(v) }));
  const hurtPts = hurt.map((v, i) => ({ x: xAt(i), y: yHurt(v) }));
  const helpLine = smoothPath(helpPts);
  const hurtLine = smoothPath(hurtPts);
  const helpFill = `${helpLine} L ${helpPts[helpPts.length - 1].x.toFixed(2)} ${baseline} L ${helpPts[0].x.toFixed(2)} ${baseline} Z`;
  const hurtFill = `${hurtLine} L ${hurtPts[hurtPts.length - 1].x.toFixed(2)} ${baseline} L ${hurtPts[0].x.toFixed(2)} ${baseline} Z`;

  // Global overlay: scale each series to the personal axis so the shape
  // shows even though absolute units differ (mood-deltas vs raw counts).
  // Each side scales to its own personal peak so a quiet personal day isn't
  // visually swamped by a loud global day on the opposite polarity.
  let benchHelpPath = '';
  let benchHurtPath = '';
  if (overlay) {
    const benchHelpMax = Math.max(1, ...overlay.helpSeries);
    const benchHurtMax = Math.max(1, ...overlay.hurtSeries);
    const personalHelpMax = Math.max(1, ...help);
    const personalHurtMax = Math.max(1, ...hurt);
    const bhHelp = overlay.helpSeries.map((v) => (v / benchHelpMax) * personalHelpMax);
    const bhHurt = overlay.hurtSeries.map((v) => (v / benchHurtMax) * personalHurtMax);
    const bhHelpPts = bhHelp.map((v, i) => ({ x: xAt(i), y: yHelp(v) }));
    const bhHurtPts = bhHurt.map((v, i) => ({ x: xAt(i), y: yHurt(v) }));
    benchHelpPath = smoothPath(bhHelpPts);
    benchHurtPath = smoothPath(bhHurtPts);
  }

  const netPts = buckets.map((b, i) => {
    const net = (b.help_mood || 0) - (b.hurt_mood || 0);
    const y = net >= 0 ? yHelp(net) : yHurt(-net);
    return { x: xAt(i), y };
  });
  const netLine = smoothPath(netPts);

  const tickIdxs = [0, Math.floor(buckets.length / 2), buckets.length - 1];
  const tickY = H + tickBand - 6;  // baseline of text inside the tick band
  const ticks = tickIdxs.map((i) => {
    const ts = buckets[i].bucket_start || 0;
    const x = xAt(i);
    return `<text x="${x.toFixed(1)}" y="${tickY}" class="sd-tick" text-anchor="${i === 0 ? 'start' : i === buckets.length - 1 ? 'end' : 'middle'}">${formatBucketLabel(ts)}</text>`;
  }).join('');

  const stripWidth = (W - padX * 2) / buckets.length;
  const strips = buckets.map((b, i) => {
    const help = b.help_mood || 0;
    const hurt = b.hurt_mood || 0;
    const tip = `${formatBucketLabel(b.bucket_start)}\nhelp ${formatN(help)} · hurt ${formatN(hurt)}`;
    return `<rect class="sd-hit-strip" x="${(xAt(i) - stripWidth / 2).toFixed(1)}" y="0" width="${stripWidth.toFixed(1)}" height="${H}" data-tip="${escapeHTML(tip)}"/>`;
  }).join('');

  return `
    <svg class="sd-pulse" viewBox="0 0 ${W} ${totalH}" preserveAspectRatio="none">
      <defs>
        <linearGradient id="sd-help-grad" x1="0" x2="0" y1="0" y2="1">
          <stop offset="0%" stop-color="var(--success)" stop-opacity="0.42"/>
          <stop offset="100%" stop-color="var(--success)" stop-opacity="0"/>
        </linearGradient>
        <linearGradient id="sd-hurt-grad" x1="0" x2="0" y1="1" y2="0">
          <stop offset="0%" stop-color="var(--danger)" stop-opacity="0.42"/>
          <stop offset="100%" stop-color="var(--danger)" stop-opacity="0"/>
        </linearGradient>
      </defs>
      <line class="sd-baseline" x1="0" y1="${baseline}" x2="${W}" y2="${baseline}"/>
      <path d="${helpFill}" fill="url(#sd-help-grad)"/>
      <path d="${hurtFill}" fill="url(#sd-hurt-grad)"/>
      ${benchHelpPath ? `<path d="${benchHelpPath}" class="sd-pulse-line sd-pulse-line-bench-help" vector-effect="non-scaling-stroke"/>` : ''}
      ${benchHurtPath ? `<path d="${benchHurtPath}" class="sd-pulse-line sd-pulse-line-bench-hurt" vector-effect="non-scaling-stroke"/>` : ''}
      <path d="${helpLine}" class="sd-pulse-line sd-pulse-line-help" vector-effect="non-scaling-stroke"/>
      <path d="${hurtLine}" class="sd-pulse-line sd-pulse-line-hurt" vector-effect="non-scaling-stroke"/>
      <path d="${netLine}" class="sd-pulse-line sd-pulse-line-net" vector-effect="non-scaling-stroke"/>
      ${ticks}
      ${strips}
    </svg>
  `;
}

function pulsePerPersona(perModelBuckets, ids) {
  const W = 696;
  const H = 220;
  const padX = 4;
  const padTop = 12;
  const padBottom = 22;
  const usableH = H - padTop - padBottom;

  const xVals = [...new Set(perModelBuckets.map((b) => b.bucket_start))].sort((a, b) => a - b);
  if (xVals.length < 2) {
    return empty('No buddy timeline in this window.');
  }
  const xIndex = new Map(xVals.map((x, i) => [x, i]));
  const xAt = (i) => padX + (i / (xVals.length - 1)) * (W - padX * 2);

  const seriesById = {};
  for (const id of ids) seriesById[id] = new Array(xVals.length).fill(0);
  for (const row of perModelBuckets) {
    if (!seriesById[row.model]) continue;
    seriesById[row.model][xIndex.get(row.bucket_start)] = (row.fires || 0) + (row.hits || 0);
  }
  const yMax = Math.max(1, ...ids.flatMap((id) => seriesById[id]));
  const yAt = (v) => padTop + (1 - v / yMax) * usableH;

  const lines = ids.map((id) => {
    const accent = personaAccent(id);
    const pts = seriesById[id].map((v, i) => ({ x: xAt(i), y: yAt(v) }));
    const path = smoothPath(pts);
    return `
      <path d="${path}" stroke="${accent}" fill="none" stroke-width="1.75"
            stroke-linecap="round" stroke-linejoin="round"
            vector-effect="non-scaling-stroke"/>
    `;
  }).join('');

  const tickIdxs = [0, Math.floor(xVals.length / 2), xVals.length - 1];
  const ticks = tickIdxs.map((i) => {
    const x = xAt(i);
    return `<text x="${x.toFixed(1)}" y="${(H - 6).toFixed(1)}" class="sd-tick" text-anchor="${i === 0 ? 'start' : i === xVals.length - 1 ? 'end' : 'middle'}">${formatBucketLabel(xVals[i])}</text>`;
  }).join('');

  const stripWidth = (W - padX * 2) / xVals.length;
  const strips = xVals.map((bs, i) => {
    const lines = ids.map((id) => `${PERSONA_LABELS[id]} ${formatN(seriesById[id][i])}`).join('\n');
    const tip = `${formatBucketLabel(bs)}\n${lines}`;
    return `<rect class="sd-hit-strip" x="${(xAt(i) - stripWidth / 2).toFixed(1)}" y="0" width="${stripWidth.toFixed(1)}" height="${H}" data-tip="${escapeHTML(tip)}"/>`;
  }).join('');

  const legend = ids.map((id) => `
    <span class="sd-legend-item" style="--accent:${personaAccent(id)};">
      <i></i>${escapeHTML(PERSONA_LABELS[id])}
    </span>
  `).join('');

  return `
    <svg class="sd-pulse" viewBox="0 0 ${W} ${H}" preserveAspectRatio="none">
      <line class="sd-baseline" x1="0" y1="${(H - padBottom).toFixed(1)}" x2="${W}" y2="${(H - padBottom).toFixed(1)}"/>
      ${lines}
      ${ticks}
      ${strips}
    </svg>
    <div class="sd-legend">${legend}</div>
  `;
}

function pulseSmallMultiples(perModelBuckets, ids) {
  // Independent y-axis per multiple: what matters here is shape, not
  // absolute comparability.
  const xVals = [...new Set(perModelBuckets.map((b) => b.bucket_start))].sort((a, b) => a - b);
  if (xVals.length < 2) return empty('No buddy timeline in this window.');
  const xIndex = new Map(xVals.map((x, i) => [x, i]));
  const seriesById = {};
  for (const id of ids) seriesById[id] = new Array(xVals.length).fill(0);
  for (const row of perModelBuckets) {
    if (!seriesById[row.model]) continue;
    seriesById[row.model][xIndex.get(row.bucket_start)] = (row.fires || 0) + (row.hits || 0);
  }
  const cells = ids.map((id) => {
    const accent = personaAccent(id);
    const series = seriesById[id];
    const total = series.reduce((a, b) => a + b, 0);
    return `
      <div class="sd-multi-cell">
        <div class="sd-multi-head">
          <span class="sd-multi-name">${escapeHTML(PERSONA_LABELS[id])}</span>
          <span class="sd-multi-num">${formatN(total)}</span>
        </div>
        <div class="sd-multi-spark" style="--accent:${accent};">
          ${gradientSparkline(series, { accent, height: 64, lineWidth: 1.4 })}
        </div>
      </div>
    `;
  }).join('');
  return `<div class="sd-multi-grid">${cells}</div>`;
}

